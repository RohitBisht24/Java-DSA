package Star_Pattern;
public class count_Triangle
{
    public static void main(String[] args)
    {
        int i, j;
        for(i=1; i<=4; i++)
        {
            for(j=1; j <= i; j++)
            {
                System.out.print(j+" ");
            }
            System.out.println();
        }
    }
}
