package Decision_Statement.if_else;

public class even_odd 
{
    public static void main(String[] args) 
    {
        int n = 12;
        
        if (n%2 == 0)
        {
            System.out.println("Even Number. ");
        }
        else 
        {
            System.out.println("Odd Number. ");
        }
    }    
}
