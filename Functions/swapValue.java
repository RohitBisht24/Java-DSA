package Functions;

public class swapValue 
{
    public static void main(String[] args) 
    {
        int a = 5;
        int b = 10;

        // swap
        int tamp = a;
        a  = b;
        b = tamp ;

        System.out.println("A is : "+a);
        System.out.println("B is : "+b);
    }
}
